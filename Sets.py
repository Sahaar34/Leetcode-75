saharsGrocery = {"Coke", "Bagels", "Cheese", "Eggs"}
friendsGrocery = {"Soap","Coke", "Meat"}

union = saharsGrocery.union(friendsGrocery)
print(f"The Union Of The Set Is: {union}")

intersection = saharsGrocery.intersection(friendsGrocery)
print(f"The Intersection Of Both The Lists Is: {intersection}")

